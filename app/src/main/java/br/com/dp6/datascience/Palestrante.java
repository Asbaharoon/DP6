class Palestrante {
    String nome;
    String empresa;
    String bio;
    int photoId;

    Palestrante(String nome, String empresa, String bio, int photoId) {
        this.nome = nome;
        this.empresa = empresa;
        this.bio = bio;
        this.photoId = photoId;
    }
}
